public class TerrainTexture {
	
	private int textureID;

	/**
	 * @param textureID
	 */
	public TerrainTexture(int textureID) {
		super();
		this.textureID = textureID;
	}

	
	/**
	 * @return the textureID
	 * @see int
	 */
	public int getTextureID() {
		return textureID;
	}
	
	
}
